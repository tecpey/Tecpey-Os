/** Returns all flashcard IDs from a term for spaced repetition initialization. */
export function extractFlashcardIds(term: Term): string[] {
  return term.modules.flatMap((m) =>
    m.lessons.flatMap((l) => l.flashcards.map((f) => f.id)),
  );
}

/** Returns a lesson by ID. */
export function getLessonById(term: Term, lessonId: string): Lesson | undefined {
  for (const mod of term.modules) {
    const lesson = mod.lessons.find((l) => l.id === lessonId);
    if (lesson) return lesson;
  }
  return undefined;
}

/** Returns whether a lesson is accessible given completion state. */
export function isLessonAccessible(
  term: Term,
  lessonId: string,
  completedLessonIds: Set<string>,
): boolean {
  for (const mod of term.modules) {
    const idx = mod.lessons.findIndex((l) => l.id === lessonId);
    if (idx === -1) continue;
    // First lesson of first module is always accessible
    if (idx === 0 && mod.moduleIndex === 1) return true;
    // Otherwise previous lesson must be completed
    if (idx > 0) {
      const prev = mod.lessons[idx - 1];
      return completedLessonIds.has(prev.id);
    }
    // First lesson of subsequent module: previous module's last lesson must be done
    const prevModule = term.modules.find((m) => m.moduleIndex === mod.moduleIndex - 1);
    if (prevModule) {
      const lastLesson = prevModule.lessons[prevModule.lessons.length - 1];
      return completedLessonIds.has(lastLesson.id);
    }
    return false;
  }
  return false;
}
