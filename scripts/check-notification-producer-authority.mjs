async function walk(directory) {
  const output = [];
  for (const entry of await readdir(directory)) {
    const absolute = path.join(directory, entry);
    const details = await stat(absolute);
    if (details.isDirectory()) {
      output.push(...(await walk(absolute)));
    } else if (/\.(?:ts|tsx|js|mjs)$/.test(entry)) {
      output.push(absolute);
    }
  }
  return output;
}

for (const absolute of await walk(sourceRoot)) {
  const relative = path.normalize(path.relative(root, absolute));
  if (relative.includes(`${path.sep}tests${path.sep}`)) continue;
  const source = await readFile(absolute, "utf8");

  if (
    !allowedDirectCreation.has(relative) &&
    source.includes("createInAppNotification")
  ) {
    failures.push(
      `${relative}: direct notification creation is forbidden; emit a governed domain event through producers.ts`,
    );
  }

  if (
    relative.startsWith(path.normalize("src/app/api/")) &&
    source.includes("produceDomainNotification")
  ) {
    failures.push(
      `${relative}: API routes may not call the producer boundary directly; integrate inside the authoritative domain transaction`,
    );
  }

  if (
    source.includes('"use client"') &&
    (source.includes("produceDomainNotification") ||
      source.includes("buildNotificationRequest"))
  ) {
    failures.push(`${relative}: notification producer authority is server-only`);
  }
}

if (failures.length) {
  console.error(
    "Notification producer authority check failed:\n- " + failures.join("\n- "),
  );
  process.exit(1);
}

